

class Pizza :
     # Almacena la variedad de la pizza
    def __init__(self, var):
        self.var = var
    
    # Permite cambiar la variedad de la pizza   
    def establecer_variedad (self, var):
        self.var = var

    # Devuelve la variedad de la pizza
    def  obtener_variedad (self):
        return self.var
    




