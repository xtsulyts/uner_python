# Definir las dos listas
lista1 = [1, 2, 3]
lista2 = [4, 5, 6]

# Crear la matriz
matriz = [lista1, lista2]

# Imprimir la matriz
print("Matriz completa:")
for fila in matriz:
    print(fila)

# Acceder a elementos específicos
print("\nAcceder a elementos específicos:")
print("Elemento en la posición [0][0]:", matriz[0][0])
print("Elemento en la posición [1][2]:", matriz[1][2])